// Use a deep string type so both locales can have different values
type DeepStringify<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? U extends string ? string[] : DeepStringify<U>[]
    : T extends object
      ? { [K in keyof T]: DeepStringify<T[K]> }
      : T;

